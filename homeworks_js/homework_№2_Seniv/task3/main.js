let number = prompt('write number');
if(number>=1 && number<=2){
    alert('Winter');
}
else if(number>=3 && number<=5){
    alert('Spring');
}
else if(number>=6 && number<=8){
    alert('Summer');
}
else if(number>=9 && number<=11){
    alert('Autumn');
}
else if(number==12){
    alert('Winter');
}
else{
    alert('Not found');
}
